public class PS_Template_Images {
    private String Images1;
    private String Images2;

    public PS_Template_Images() {
    }

    public PS_Template_Images(String images1, String images2) {
        Images1 = images1;
        Images2 = images2;
    }

    public String getImages1() {
        return Images1;
    }

    public void setImages1(String images1) {
        Images1 = images1;
    }

    public String getImages2() {
        return Images2;
    }

    public void setImages2(String images2) {
        Images2 = images2;
    }
}
